public class OperadorTernario {
    public static void main(String[] args) {
        int n1, n2, r;
        n1 = 4;
        n2 = 5;
        //Funcionamento do operador ternário
        // N1 é maior que N2? Se sim, r recebe 0 senão, r recebe 1.
        r = (n1>n2)?n1:n2;
        System.out.println(r);
    }
}
